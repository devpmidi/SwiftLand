// Obter o modal e o botão de abrir
var modal = document.getElementById("socialModal");
var btn = document.getElementById("followBtn");
var span = document.getElementById("closeBtn");

// Quando o usuário clicar no botão, abrir o modal
btn.onclick = function() {
    modal.style.display = "block";
}

// Quando o usuário clicar no "x", fechar o modal
span.onclick = function() {
    modal.style.display = "none";
}

// Quando o usuário clicar em qualquer lugar fora do modal, fechar o modal
window.onclick = function(event) {
    if (event.target == modal) {
        modal.style.display = "none";
    }
}

